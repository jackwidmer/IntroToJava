public class WidmerMileConversions {
        static final int MILE = 1;
        public static void main(String[] args) {
            int milesToFeet = MILE * 5280;
            int milesToYards = MILE * 1760;
            System.out.println(MILE + " mile(s) in feet is: " + milesToFeet + " feet.");
            System.out.println(MILE + " mile(s) in yards is: " + milesToYards + " yards.");
    }
}
